        <p>VIKOR (VIÅ¡ekriterijumsko KOmpromisno Rangiranje dalam bahasa Serbia, yang artinya Perangkingan Kompromis MultiKriteria) adalah metode perankingan dengan menggunakan indeks peringkat multikriteria berdasarkan ukuran tertentu dari kedekatan dengan solusi yang ideal. Metode VIKOR merupakan salah satu metode yang dapat dikategorisasikan dalam Multi-Criteria Decision Analysis/MCDA (Opricovic 1998). Metode VIKOR dikembangkan sebagai metode Multi-Criteria Decision Making/MCDM untuk menyelesaikan pengambilan keputusan bersifat diskrit pada kriteria yang bertentangan dan non-commensurable(tidak ada cara yang tepat untuk menentukan mana yang lebih akurat) (Opricovic and Tzeng 2007).</p>
        <p>Langkah-langkah yang digunakan dalam Metode VIKOR adalah sebagai berikut (Kusdiantoro 2012): <br>
    1. Membuat Matriks Keputusan (F)<br>    
    2. Menentukan Bobot Kriteria (W)<br>
    3. Membuat Matriks Normalisasi (N)<br>
    4. Normalisasi Bobot (F*)<br>
    5. Menghitung Utility Measures(S) dan Regret Measures(R)<br>
    6. Menghitung indeks VIKOR (Q)<br>
    7. Perankingan alternatif<br>
        </p>
<p> Kelebihan dan Kekurangan :<br>
   1. Kelebihan : metode VIKOR memiliki kelebihan pada proses pemeringkatan dengan memiliki nilai preferensi untuk pemeringkatan dan dapat mengatasi pemeringkatan banyak alternatif dengan lebih mudah.
    Selain itu, metode VIKOR memiliki kelebihan mengatasi kriteria yang bertentangan dalam pemeringkatan. Kriteria bertentangan yang dimaksud adalah terdapat beberapa kriteria tetapi masing-masing kriteria tersebut menggunakan penilaian berbeda. Penilaian itu dapat melihat nilai tertinggi semakin baik atau nilai terendah semakin baik.<br>
    2. kekurangan :
    pada tahap pembobotan, proses pembobotan hanya diberikan begitu saja oleh atasan/pengambil keputusan tanpa adanya cek konsistensi pembobotan seperti metode AHP

</p>
    

